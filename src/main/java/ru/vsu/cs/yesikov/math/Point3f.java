package ru.vsu.cs.yesikov.math;

public class Point3f {

    private final float x;
    private final float y;
    private final float z;

    public Point3f(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }

}

