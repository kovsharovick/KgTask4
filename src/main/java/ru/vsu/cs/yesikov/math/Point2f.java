package ru.vsu.cs.yesikov.math;

public class Point2f {

    private final float x;
    private final float y;

    public Point2f(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

}
